#include "libft.h"

void	ft_reverse(char *s)
{
	int i;
	int j;
	int c;

	i = 0;
	j = ft_strlen(s) - 1;
	while (*s && i++ < j--)
	{
		c = s[i];
		s[i] = s[j];
		s[j] = c;
	}
}
