#include "libft.h"

char	*ft_strmap(char const *s, char (*f)(char))
{
	unsigned int	i;
	char			*fresh;

	i = 0;
	if (!s && !f)
		return (NULL);
	if (!(fresh = (char *)malloc(sizeof(*s) * (ft_strlen(s)) + 1)))
		return (NULL);
	while (s[i] != '\0')
	{
		fresh[i] = f(s[i]);
		i++;
	}
	fresh[i] = '\0';
	return (fresh);
}
