#include "libft.h"

char	*ft_strdup(const char *s)
{
	size_t	i;
	char	*temp;

	i = 0;
	while (s[i])
		i++;
	if (!(temp = (char*)malloc(sizeof(char) * (i + 1))))
		return (NULL);
	i = 0;
	while (s[i])
	{
		temp[i] = s[i];
		i++;
	}
	temp[i] = '\0';
	return (temp);
}
